func TestCopilotComposer_HeaderPresent(t *testing.T) {
	root := t.TempDir()
	repo := &arslib.Repository{
		Manifest: arslib.Manifest{Project: arslib.Project{Name: "demo"}},
		Instructions: []arslib.Instruction{{
			ID:      "repo",
			Path:    ".ai/instructions/repo.md",
			Content: "Repository rules.\n",
		}},
		Skills: []arslib.Skill{{
			ID:      "task-implementation",
			Path:    ".ai/skills/task-implementation/SKILL.md",
			Content: "# Task Implementation\n\n## Purpose\nImplement one task at a time.\n",
		}},
		Prompts: []arslib.Prompt{{
			ID:      "create-plan",
			Path:    ".ai/prompts/create-plan.md",
			Content: "Create a detailed implementation plan.\n",
		}},
		Agents: []arslib.Agent{{
			ID:      "planner",
			Path:    ".ai/agents/planner/AGENT.md",
			Content: "## Role\nPlans work.\n",
			SkillRefs: []string{
				"skills/task-implementation/SKILL.md",
			},
		}},
	}

	require.NoError(t, (&CopilotComposer{}).Compose(root, repo))

	data, err := safepath.ReadFile(root, ".github/copilot-instructions.md")
	require.NoError(t, err)
	assert.Contains(t, string(data), "<!-- Generated by ars")
	assert.Contains(t, string(data), ".github/skills/*/SKILL.md")

	instructionData, err := safepath.ReadFile(root, ".github/instructions/repo.instructions.md")
	require.NoError(t, err)
	assert.Contains(t, string(instructionData), "applyTo: \"**\"")

	skillData, err := safepath.ReadFile(root, ".github/skills/task-implementation/SKILL.md")
	require.NoError(t, err)
	assert.Contains(t, string(skillData), "name: task-implementation")
	assert.Contains(t, string(skillData), "description:")

	promptData, err := safepath.ReadFile(root, ".github/prompts/create-plan.prompt.md")
	require.NoError(t, err)
	assert.Contains(t, string(promptData), "description:")
	assert.NotContains(t, string(promptData), "mode:")
	assert.NotContains(t, string(promptData), "<!-- ars:source")

	agentData, err := safepath.ReadFile(root, ".github/agents/planner.agent.md")
	require.NoError(t, err)
	assert.Contains(t, string(agentData), "target: github-copilot")
	assert.Contains(t, string(agentData), "name: planner")
}

