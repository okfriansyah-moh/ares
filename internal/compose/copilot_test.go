func TestCopilotComposer_SkillInlined(t *testing.T) {
	root := t.TempDir()
	repo := &arslib.Repository{
		Manifest: arslib.Manifest{Project: arslib.Project{Name: "demo"}},
		Agents: []arslib.Agent{{
			ID:        "architect",
			Path:      ".ai/agents/architect/AGENT.md",
			Content:   "## Responsibilities\nDesign systems.\n",
			SkillRefs: []string{"skills/architecture-management/SKILL.md"},
		}},
		Skills: []arslib.Skill{{
			ID:      "architecture-management",
			Path:    ".ai/skills/architecture-management/SKILL.md",
			Content: "Architecture skill body.",
		}},
	}

	require.NoError(t, (&CopilotComposer{}).Compose(root, repo))

	data, err := safepath.ReadFile(root, ".github/copilot-instructions.md")
	require.NoError(t, err)
	body := string(data)
	agentIdx := strings.Index(body, "## Agent: architect")
	require.GreaterOrEqual(t, agentIdx, 0)
	agentSection := body[agentIdx:]
	assert.Contains(t, agentSection, "Architecture skill body.")

	agentProfile, err := safepath.ReadFile(root, ".github/agents/architect.agent.md")
	require.NoError(t, err)
	assert.Contains(t, string(agentProfile), "Architecture skill body.")
}

func TestCopilotComposer_Idempotent(t *testing.T) {
	root := t.TempDir()
	repo := &arslib.Repository{
		Manifest: arslib.Manifest{Project: arslib.Project{Name: "demo"}},
		Instructions: []arslib.Instruction{{
			ID:      "rules",
			Content: "Repository rules.",
		}},
		Agents: []arslib.Agent{{
			ID:      "planner",
			Path:    ".ai/agents/planner/AGENT.md",
			Content: "Plans work.",
		}},
		Prompts: []arslib.Prompt{{
			ID:      "plan",
			Path:    ".ai/prompts/plan.md",
			Content: "Plan this change.",
		}},
	}

	composer := &CopilotComposer{}
	require.NoError(t, composer.Compose(root, repo))

	first, err := safepath.ReadFile(root, ".github/copilot-instructions.md")
	require.NoError(t, err)

	require.NoError(t, composer.Compose(root, repo))
	second, err := safepath.ReadFile(root, ".github/copilot-instructions.md")
	require.NoError(t, err)

	assert.Equal(t, first, second)
}

